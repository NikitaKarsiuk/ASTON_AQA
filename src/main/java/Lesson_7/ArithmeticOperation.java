package Lesson_7;

public class ArithmeticOperation {
    public static int addition(int value_1, int value_2){
        return value_1 + value_2;
    }
    public static int subtraction(int value_1, int value_2){
        return  value_1 - value_2;
    }
    public static int multiplication(int value_1, int value_2){
        return  value_1 * value_2;
    }
    public static double divison(int value_1, int value_2){
        return (double) value_1 / value_2;
    }
}
